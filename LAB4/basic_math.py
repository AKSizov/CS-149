def add(x, y):
    """Add - sum of x and y.

    Args:
        x(float): first number
        y(float): second number

    Returns:
        float: sum of x and y
    """
    s = x + y
    return s


def subtract(x, y):
    """Difference between x and y.

    Args:
        x(float): first number
        y(float): second number

    Returns:
        float: difference between x and y
    """
    diff = x - y
    return diff


def multiply(x, y):
    """Add - multiply x and y.

    Args:
        x(float): first number
        y(float): second number

    Returns:
        float: rseult of x and y
    """
    m = x * y
    return m


def divide(x, y):
    """Divide x and y.

    Args:
        x(float): first number
        y(float): second number

    Returns:
        float: result between x and y
    """
    d = x / y
    return d
